package li.ste.adventofcode.year2021.day19;

public class Rotation {
    private final int x;
    private final int y;
    private final int z;

    public Rotation(int x, int y, int z) {
        this.x = x;
        this.y = y;
        this.z = z;
    }

    public int getX() {
        return x;
    }
    public int getY() {
        return y;
    }
    public int getZ() {
        return z;
    }
}
